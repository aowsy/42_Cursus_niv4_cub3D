#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	m;
	char	i;

	m = 45;
	if (n <= -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write (fd, &m, 1);
		n = n * -1;
	}
	if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	else
	{
		i = n + 48;
		write (fd, &i, 1);
	}
}
